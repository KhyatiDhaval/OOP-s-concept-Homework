package homework3;

public class OopsConcept {

    int a = 20;
    int b = 35;

    public static void main(String[] args) {

        OopsConcept myObj = new OopsConcept();

        System.out.println(myObj.a + myObj.b);
        System.out.println(myObj.b - myObj.a);
        System.out.println(myObj.a * myObj.b);
        System.out.println(myObj.b / myObj.a);
        System.out.println(myObj.b % myObj.a);
    }
}
